struct dgvideo {
	uint8_t ptr;
	uint8_t mem[256];	/* 6bit wide */
	uint8_t last;
	uint32_t raster[128 * 256];
};

/* 8 pixels per char X = 256 pixels a scan line,  4096 pixels a character
   row  */

static void dg_raster(struct dgvideo *dg, unsigned int byte)
{
	uint8_t c = dg->mem[byte] & 0x7F;
	const uint8_t *fontptr = dg_font + 16 * c;
	uint32_t *rptr =
		dg->raster + ((byte & 0xE0) << 7) + ((byte & 0x1F) << 3);
	unsigned int x, y;
	uint8_t bits;

	for (y = 0; y < 16; y++) {
		bits = *fontptr++;
		for (x = 0; x < 8; x++) {
			if (bits & 0x80)
				*rptr++ = 0xFFAAAAAA;
			else
				*rptr++ = 0xFF222222;
			bits <<= 1;
		}
		rptr += 248;	/* We moved on 8 in the loop */
	}
}

/* Write only device */
void dgvideo_write(struct dgvideo *dg, uint8_t val)
{
	dg->mem[dg->ptr] = val & 0x7F;
	if (val == 0xFF) {
		dg->ptr = 0xFF;
	}
	if (!((dg->last ^ val) & val & 0x80)) {
		/* Bit 6 high to lowclocked ptr */
		dg->ptr++;
	}
	dg->last = val;
}

/* For now this is wildly approximate but it looks good */
void dgvideo_noise(struct dgvideo *dg, unsigned int cycles, uint8_t val)
{
	/* There are 10000 cycles per half video frame  (interlanced)
	   There are 262.5 lines per frame (NTSC) and there are
	   38 machine clocks per scan line. Roughly speaking we need to divide
	   the 38 machine clocks into 64us, the first 6.2 and last 4.5 of which
	   are skipped and vertically on lines 1-21 or so */
	unsigned int line = cycles / 38;
	unsigned int p;
	unsigned int x;
	uint8_t byte;
	uint32_t *rp;
	if (line < 20)
		return;
	printf("cycle %d line %d\n", cycles, line);
	cycles -= 21 * 38;
	cycles %= 38;
	if (cycles < 3 || cycles >= 35)
		return;
	cycles -= 3;
	line -= 20;
	/* We have 242 lines but only 128 we care about */
	if (line < 57)
		return;
	line -= 57;
	if (line > 127)
		return;

	/* Now it's a horizontal byte for that line */
	for (p = 0; p < 1; p++) {
		rp = dg->raster + 256 * line + 8 * cycles;
		byte = val;
		for (x = 0; x < 8; x++) {
			if (byte & 0x80)
				*rp++ = 0xFFAAAAAA;
			else
				*rp++ = 0xFF222222;
			byte <<= 1;
		}
		cycles++;
	}
}

void dgvideo_rasterize(struct dgvideo *dg)
{
	unsigned int i;
	for (i = 0; i < 256; i++) {
		dg_raster(dg, i);
	}
}

struct dgvideo *dgvideo_create(void)
{
	struct dgvideo *dg = malloc(sizeof(struct dgvideo));
	if (dg == NULL) {
		fprintf(stderr, "Out of memory.\n");
		exit(1);
	}
	dg->ptr = 0;
	memset(dg->mem, 'A', 256);
	dgvideo_rasterize(dg);
	return dg;
}

void dgvideo_free(struct dgvideo *dg)
{
	free(dg);
}

uint32_t *dgvideo_get_raster(struct dgvideo *dg)
{
	return dg->raster;
}
